import type { GameplayGoal, QuestAct, QuestChoice, QuestTier, RichQuest } from "./questTypes";

type MainQuestDraft = {
  id: string;
  title: string;
  act: QuestAct;
  tier: QuestTier;
  premise: string;
  hook: string;
  emotional: string;
  merchant: string;
  giver: string;
  npcs?: string[];
  origin: string;
  targets?: string[];
  goals: GameplayGoal[];
  stages: string[];
  choices: readonly [string, string, QuestChoice["approach"], string, string][];
  success: string;
  failure?: string;
};

function consequence(label: string, target: string, amount = 1) {
  return { type: "ending_score_change" as const, label, target, amount };
}

function choice([id, label, approach, description, consequenceLabel]: MainQuestDraft["choices"][number]): QuestChoice {
  return {
    id,
    label,
    approach,
    description,
    consequences: [consequence(consequenceLabel, approach, approach === "exploit" || approach === "shadow" ? 2 : 1)],
  };
}

function quest(draft: MainQuestDraft): RichQuest {
  return {
    id: draft.id,
    title: draft.title,
    arc: "The Ledger That Bought a City",
    act: draft.act,
    category: "main_campaign",
    tier: draft.tier,
    storyPremise: draft.premise,
    playerHook: draft.hook,
    emotionalConflict: draft.emotional,
    merchantConflict: draft.merchant,
    giverNpcId: draft.giver,
    importantNpcIds: [draft.giver, ...(draft.npcs || [])],
    originMarketId: draft.origin,
    targetMarketIds: draft.targets,
    openingScene: {
      location: draft.origin,
      mood: "merchant intrigue",
      summary: draft.hook,
      dialogue: draft.hook,
    },
    stages: draft.stages.map((stage, index) => ({
      id: `${draft.id}-stage-${index + 1}`,
      title: stage.split(" — ")[0] || `Stage ${index + 1}`,
      story: stage,
      objective: stage.includes(" — ") ? stage.split(" — ").slice(1).join(" — ") : stage,
    })),
    choices: draft.choices.map(choice),
    consequences: [consequence(`${draft.title} changes the ledger's political weight.`, "ledger_weight")],
    successEnding: draft.success,
    failureEnding: draft.failure,
    gameplayGoals: draft.goals,
    requirements: [{ type: "inspect_ledger", label: "Read the relevant ledger page.", target: draft.id }],
    rewards: [
      { type: "reputation", label: "Merchant reputation changes based on the chosen solution.", target: "merchant_reputation", amount: 1 },
      { type: "ending_score", label: "The chosen approach shifts possible campaign endings.", target: "campaign_alignment", amount: 1 },
    ],
    unlocks: [],
    repeatable: false,
    isMainQuest: true,
    verticalSlice: ["main-02-first-honest-profit", "main-11-the-false-scale", "main-16-a-name-on-the-door", "main-18-warehouse-lease"].includes(draft.id),
  };
}

