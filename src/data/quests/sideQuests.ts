import type { GameplayGoal, QuestChoice, QuestTier, SideQuestConcept } from "./questTypes";

type SideDraft = {
  id: string;
  title: string;
  family: string;
  tier: QuestTier;
  premise: string;
  hook: string;
  pressure: string;
  conflict: string;
  giver: string;
  origin: string;
  targets?: string[];
  goals: GameplayGoal[];
  choices: readonly [string, string, QuestChoice["approach"], string][];
};

function sideChoice([id, label, approach, description]: SideDraft["choices"][number]): QuestChoice {
  return {
    id,
    label,
    approach,
    description,
    consequences: [
      {
        type: approach === "honest" ? "city_reputation_change" : approach === "exploit" || approach === "shadow" ? "legal_heat_change" : "npc_trust_change",
        label: description,
        target: approach,
        amount: approach === "exploit" || approach === "shadow" ? 2 : 1,
      },
    ],
  };
}

function side(draft: SideDraft): SideQuestConcept {
  return {
    id: draft.id,
    title: draft.title,
    family: draft.family,
    arc: "Regional Merchant Stories",
    category: draft.family.includes("shortage") ? "shortage_relief" : draft.family.includes("warehouse") ? "warehouse_problem" : draft.family.includes("auction") ? "auction" : draft.family.includes("route") ? "route_unlock" : "npc_favor",
    tier: draft.tier,
    storyPremise: draft.premise,
    playerHook: draft.hook,
    emotionalConflict: draft.pressure,
    merchantConflict: draft.conflict,
    giverNpcId: draft.giver,
    importantNpcIds: [draft.giver],
    originMarketId: draft.origin,
    targetMarketIds: draft.targets,
    openingScene: { location: draft.origin, summary: draft.hook, dialogue: draft.hook, mood: "local pressure" },
    stages: [
      { id: `${draft.id}-stage-1`, title: "Hear the pressure", story: draft.premise, objective: "Learn who is affected and why the problem cannot wait." },
      { id: `${draft.id}-stage-2`, title: "Trace the trade problem", story: draft.conflict, objective: "Find the item, route, contract, or person causing the pressure." },
      { id: `${draft.id}-stage-3`, title: "Choose a merchant solution", story: draft.pressure, objective: "Resolve the problem through trade, leverage, reputation, or profit." },
    ],
    choices: draft.choices.map(sideChoice),
    consequences: [{ type: "market_event", label: `${draft.title} leaves a local market memory.`, target: draft.origin, amount: 1 }],
    successEnding: `${draft.title} resolves according to the player's chosen merchant approach, affecting local trust and future opportunity.`,
    gameplayGoals: draft.goals,
    requirements: [{ type: "talk_to_npc", label: "Speak with the quest giver.", npcId: draft.giver }],
    rewards: [{ type: "reputation", label: "Local reputation shifts with the chosen resolution.", target: draft.origin, amount: 1 }],
    repeatable: false,
    isMainQuest: false,
    verticalSlice: draft.id === "side-01-bread-before-dawn",
  };
}

